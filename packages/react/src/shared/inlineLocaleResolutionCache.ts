/**
 * Resolves a locale string to its canonical BCP 47 form using the
 * pre-computed lookup table. Falls back to Intl.Locale for unknown inputs.
 */
export function resolveLocale(locale: string): string {
  const cached = LOCALE_NORMALIZATION_TABLE[locale];
  if (cached) return cached;

  // Fallback: try case-insensitive lookup
  const lower = locale.toLowerCase();
  const fallback = LOCALE_NORMALIZATION_TABLE[lower];
  if (fallback) return fallback;

  // Ultimate fallback: use Intl.Locale
  try {
    const resolved = new Intl.Locale(locale);
    return resolved.toString();
  } catch {
    return locale;
  }
}

/**
 * Returns the script for a given locale using pre-computed table.
 */
export function getLocaleScript(locale: string): string | undefined {
  const normalized = resolveLocale(locale);
  const base = normalized.split('-')[0];
  return LOCALE_SCRIPT_TABLE[normalized] ?? LOCALE_SCRIPT_TABLE[base];
}

/**
 * Returns the text direction for a given locale.
 * Defaults to 'ltr' for non-RTL locales.
 */
export function getLocaleDirection(locale: string): 'ltr' | 'rtl' {
  const normalized = resolveLocale(locale);
  if (LOCALE_DIRECTION_TABLE[normalized]) return 'rtl';
  const base = normalized.split('-')[0];
  if (LOCALE_DIRECTION_TABLE[base]) return 'rtl';
  return 'ltr';
}

/**
 * Returns the plural categories for a given locale.
 */
export function getLocalePluralCategories(locale: string): readonly string[] {
  const normalized = resolveLocale(locale);
  const base = normalized.split('-')[0];
  return LOCALE_PLURAL_CATEGORIES[base] ?? ['one', 'other'];
}

/**
 * Returns number formatting metadata for a locale.
 */
export function getLocaleNumberFormat(
  locale: string
): readonly [string, string, 'prefix' | 'suffix'] {
  const normalized = resolveLocale(locale);
  return (
    LOCALE_NUMBER_FORMAT[normalized] ??
    LOCALE_NUMBER_FORMAT[normalized.split('-')[0]] ?? ['.', ',', 'prefix']
  );
}

/**
 * Checks if a locale is supported by the pre-computed tables.
 */
export function isPrecomputedLocale(locale: string): boolean {
  return locale in LOCALE_NORMALIZATION_TABLE || locale.toLowerCase() in LOCALE_NORMALIZATION_TABLE;
}

/**
 * Returns the total number of pre-computed locale entries.
 * Useful for diagnostics and cache hit rate estimation.
 */
export function getPrecomputedLocaleCount(): number {
  return Object.keys(LOCALE_NORMALIZATION_TABLE).length;
}
